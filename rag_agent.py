    def retrieve_context(self, query: str, difficulty: str = "中等") -> str:
        if difficulty == "进阶":
            top_k = self.base_top_k * 4
            extend_query = f"{query} 深层原理 应用场景 对比辨析 综合案例 关联知识点 易错点 考情分析"
        else:
            top_k = self.base_top_k
            extend_query = query

        docs_base = self.vector_store.search(query, top_k=top_k)
        docs_extend = self.vector_store.search(extend_query, top_k=top_k) if difficulty == "进阶" else []

        all_docs = docs_base + docs_extend
        seen_content = set()
        unique_docs = []
        for doc in all_docs:
            content = doc.get("content", "").strip()
            if content not in seen_content and content:
                seen_content.add(content)
                unique_docs.append(doc)

        context = []
        for i, doc in enumerate(unique_docs[:top_k * 2]):
            content = doc.get("content", "").strip()
            metadata = doc.get("metadata", {})
            filename = metadata.get("filename", "unknown")
            content = self.page_forbidden_pattern.sub('', content)
            context.append(f"[材料 {i+1}] 来源: {filename}\n内容: {content}")

        return "\n\n".join(context)

